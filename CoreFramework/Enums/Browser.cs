namespace CoreFramework.Enums;

/// <summary>
/// Represents the types of browsers supported by the framework.
/// </summary>
public enum Browser
{
    /// <summary>
    /// Represents a generic Chromium-based browser.
    /// </summary>
    Chromium,

    /// <summary>
    /// Represents the Google Chrome browser, a channel of Chromium.
    /// </summary>
    Chrome,

    /// <summary>
    /// Represents the Microsoft Edge browser, a channel of Chromium.
    /// </summary>
    Edge,

    /// <summary>
    /// Represents the Mozilla Firefox browser.
    /// </summary>
    Firefox,

    /// <summary>
    /// Represents the Apple Safari browser, also referred to as Webkit.
    /// </summary>
    Webkit
}

